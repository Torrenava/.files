#!/bin/bash

# Instalador Plugins Micro

micro -plugin install fzf # Abre Archivos usando fzf. Ctrl + E -> fzf -> Selecciona el archivo.
micro -plugin install quoter # Permite poner comillas al texto seleccionado.
micro -plugin install detectindent # Detecta y configura la identación automáticamente.
micro -plugin install filemanager # https://github.com/NicolaiSoeborg/filemanager-plugin
micro -plugin install palettero # Ctrl + P para Paleta de Comandos
micro -plugin install cheat # F1 para info de atajos de teclado.
